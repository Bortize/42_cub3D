#include "../headers/cub3d.h"

int find_identifier(char *str, map_config *map)
{
    int i;
    char identifier;

    i = 0;
    while (str[i])
    {
        if (ft_strchr("RSCF", str[i]))
            identifier = str[i];
            check_identifier(identifier, map);
        i++;
    }
    return (0);
}